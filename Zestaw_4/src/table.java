public class table {
    public static void show_int(int[] tab)
    {
        for(int i=0;i < tab.length;i++)
        {
            System.out.print(tab[i] + " ");
        }
    }
    public static void show_d(double[] tab)
    {
        for(int i=0;i < tab.length;i++)
        {
            System.out.print(tab[i] + " ");
        }
    }
}
